::OUTER_LOOP
    copy 2 0 0 $COUNTER $TMP1
    copy 2 0 0 $COUNTER $TMP2
    copy 2 0 0 $COUNTER $TMP3
    copy 2 0 0 $COUNTER $TMP4
    store 2 0 0 0 $TMP4
    pack8 5 0 0 $TMP1 $TMP2 $TMP3 $TMP4 $COLOR_VALUE
